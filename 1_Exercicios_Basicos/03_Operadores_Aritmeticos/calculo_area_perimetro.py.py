print("****  Calculadore de Área de Retângulo ****")

# º1 DECLARE DUAS VARIÁVEIS, largura e altura, E ATRIBUA A ELAS VALORES NUMÉRICOS (POR EXEMPLO,10 E 5).
# º2 USE OS OPERADORES ARITMÉTICOS PARA CALCULAR A area(LARGURA * ALTURA) E O perimetro(2*(LARGURA + ALTURA)) DO RETÂNGULO.
# º3 IMPRIMA OS RESUKTADOS DE FORMA CLARA, PRO EXEMPLO:"a área do retângulo é: [valor de area]" e "o perimetro do retângulo é:[valor do perimetro]".

largura = 10 
altura = 5 

area = largura * altura

perimetro = (2* (largura + altura))

print(f"A área do retângulo é:{area} \nO perimetro do retângulo é:{perimetro}")